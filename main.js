function startPythonBackend() {
    let scriptPath;
    let pythonExecutable = 'python3';

    if (isDev) {
        scriptPath = path.join(__dirname, 'backend', 'server.py');
        pythonExecutable = path.join(__dirname, 'backend', 'venv', 'bin', 'python');
    } else {
        scriptPath = path.join(process.resourcesPath, 'backend', 'server.py');
        pythonExecutable = path.join(process.resourcesPath, 'backend', 'venv', 'bin', 'python');
    }

    console.log('Starting Python backend from:', scriptPath);

    pythonProcess = spawn(pythonExecutable, [scriptPath]);

    pythonProcess.stdout.on('data', (data) => {
        console.log(`Backend stdout: ${data}`);
    });

    pythonProcess.stderr.on('data', (data) => {
        console.error(`Backend stderr: ${data}`);
    });

    pythonProcess.on('close', (code) => {
        console.log(`Backend process exited with code ${code}`);
    });
}

const configDir = path.join(app.getPath('home'), '.social-video-utility');
const accountsFile = path.join(configDir, 'accounts.json');

// FRESH START: Delete all saved cookies and account data on every launch
try {
    if (fs.existsSync(configDir)) {
        const files = fs.readdirSync(configDir);
        for (const file of files) {
            const filePath = path.join(configDir, file);
            if (fs.lstatSync(filePath).isDirectory()) {
                // Skip directories for now or recursively delete
            } else {
                fs.unlinkSync(filePath);
            }
        }
        console.log('Startup cleanup: Wiped config directory contents');
    }
} catch (e) {
    console.error('Startup cleanup error:', e);
}

// Track current partition name (initial one is unique)
let currentPartition = `twitter-init-${Date.now()}`;

// Accounts Manager
const accountsManager = {
    getAccounts: () => {
        try {
            if (fs.existsSync(accountsFile)) {
                return JSON.parse(fs.readFileSync(accountsFile, 'utf8'));
            }
        } catch (e) { console.error('Error reading accounts:', e); }
        return { accounts: {}, activeAccount: null };
    },

    saveAccounts: (data) => {
        try {
            fs.writeFileSync(accountsFile, JSON.stringify(data, null, 2));
        } catch (e) { console.error('Error saving accounts:', e); }
    },

    updateAccount: (profile, cookies) => {
        const data = accountsManager.getAccounts();
        const cookieFileName = `cookies_${profile.username}.txt`;
        const cookieFilePath = path.join(configDir, cookieFileName);

        // Save cookies specific to this user
        let cookieString = '# Netscape HTTP Cookie File\n# Generated by Social Video Utility\n\n';
        cookies.forEach((cookie) => {
            const domain = cookie.domain.startsWith('.') ? cookie.domain : '.' + cookie.domain;
            cookieString += `${domain}\tTRUE\t${cookie.path}\t${cookie.secure.toString().toUpperCase()}\t${cookie.expirationDate}\t${cookie.name}\t${cookie.value}\n`;
        });
        fs.writeFileSync(cookieFilePath, cookieString);

        // Also update main cookies.txt if this is the active user (or becoming active)
        if (!data.activeAccount || data.activeAccount === profile.username) {
            fs.writeFileSync(path.join(configDir, 'cookies.txt'), cookieString);
        }

        data.accounts[profile.username] = {
            ...profile,
            cookieFile: cookieFileName,
            lastSeen: Date.now()
        };
        data.activeAccount = profile.username;
        accountsManager.saveAccounts(data);
        return data;
    },

    setActiveAccount: async (username, session) => {
        const data = accountsManager.getAccounts();
        const account = data.accounts[username];
        if (!account) return false;

        const cookiePath = path.join(configDir, account.cookieFile);
        if (!fs.existsSync(cookiePath)) return false;

        // Clear current cookies
        await session.clearStorageData({ storages: ['cookies'] });

        // Load target cookies
        const content = fs.readFileSync(cookiePath, 'utf8');
        const lines = content.split('\n');

        for (const line of lines) {
            if (line.startsWith('#') || !line.trim()) continue;
            const parts = line.split('\t');
            if (parts.length >= 7) {
                const cookie = {
                    url: 'https://' + parts[0].replace(/^\./, ''),
                    name: parts[5],
                    value: parts[6],
                    domain: parts[0],
                    path: parts[2],
                    secure: parts[3] === 'TRUE',
                    expirationDate: parseFloat(parts[4])
                };
                try {
                    await session.cookies.set(cookie);
                } catch (e) { /* ignore */ }
            }
        }

        // Update active account
        data.activeAccount = username;
        accountsManager.saveAccounts(data);

        // Sync main cookies.txt
        fs.copyFileSync(cookiePath, path.join(configDir, 'cookies.txt'));

        return true;
    }
};

